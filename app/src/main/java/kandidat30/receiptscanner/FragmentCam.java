    @Override
    public boolean onTouch(View v, MotionEvent event) {
        //down = start, event = now
        if(event.getEventTime() > event.getDownTime() + 1000 && !isHold){
            isHold = true;
            startRecording();
        }
        switch (event.getAction()) {
            case MotionEvent.ACTION_UP:
                if(isHold) {
                    Log.d(TAG, "stop recording ontouch");
                    stopRecording();
                    isHold = false;
                }
                else {
                    Log.d(TAG, "take pic");
                    takePicture();
                }
                break;
        }
        return true;

    }

